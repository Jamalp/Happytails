Class Person
  attr_accessor :name, :age, :gender, :kids, :num_of_pets
  def initialize
    @name = name
    @age = age
    @gender = gender
    @kids = kids
    @num_of_pets = []
  end
end

Class Animal :name, :breed, :age, :gender, :fav_toys
  def initialize
    @name = name
    @breed = breed
    @age = age
    @gender = gender
    @fav_toys = fav_toys
  end
end
